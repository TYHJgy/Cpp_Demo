unsigned char *dw_des_encrypt
	(	
		const char *in_src, //输入明文
		const int in_size,//明文长度
		const char *in_key,
		unsigned char *out_dst, //输出密文
		int *out_size //密文长度
	)
{
	int key_len = 0; //密匙长度
	unsigned char key[DES_KEY_SIZE] = {0}; /* 截取或补齐后的密钥 */
	const char *data = in_src;
	int data_rest = 0;
	unsigned char ch; 		/*补齐用的字符*/
	unsigned char *src = NULL; 		/* 补齐后的明文 */
	int len; 	/*补齐后的明文长度*/
	unsigned char tmp[8];
	des_context ctx;


	__des_init( &ctx );
	
	key_len = strlen(in_key);
    if ( key_len >= 8 )
    {
    	memcpy(key, in_key, 8);
    }
	else
    {
    	memcpy(key, in_key, key_len);
    	memset(key + key_len, 0x00, DES_KEY_SIZE - key_len);
    }

	/* 分析补齐明文所需空间及补齐填充数据 */
    data_rest = in_size % 8;
    len = in_size + (8 - data_rest);
	ch = 8 - data_rest;
	*out_size = len;

	out_dst = (unsigned char*)realloc(out_dst , len); //明文长度有补齐字节，必须重新分配
	memset(out_dst, 0x00, len);

	src = (unsigned char *)malloc(len);
	if ( src != NULL )
	{
		int count;
        int i;
		/* 构造补齐后的加密内容 */
        memset(src, 0x00, len);
        memcpy(src, data, in_size);
        memset(src + in_size, ch, 8 - data_rest);

		/*set key*/
		__des_setkey_enc( &ctx, key );

		/* 循环加密/解密，每8字节一次 */
		count = len / 8;
        for (i = 0; i < count; i++)
        {
            memset(tmp, 0, 8);
            memcpy(tmp, src + 8 * i, 8);
            /* 加密 */
            __des_crypt_ecb( &ctx, tmp, out_dst+ 8 * i );
        }
		
	}

	if (NULL != src)
    {
        free(src);
        src = NULL;
    }

	__des_free( &ctx );
   
    return out_dst;
}

